## よくある抜け漏れTOP5

1. **在留カードへの住所記載を忘れる**　住民票登録後、在留カード裏面に市区町村が住所を記載する。記載前は銀行口座が開設できない場合がある

2. **マイナンバーの通知が届かない**　転居時に住所変更届を出し忘れると届かない。宿舎が会社名義の場合は特に要注意

3. **社会保険の加入日がずれる**　雇用開始日から加入義務。入国日と雇用開始日が異なる場合に漏れが出やすい

4. **給与初回支払いに銀行口座が間に合わない**　口座開設には2週間以上かかる場合がある。現金支払いの準備をしておく

5. **緊急時の連絡体制が未整備**　深夜・休日の体調不良・事故に対応できる体制を入国前に整えておく

