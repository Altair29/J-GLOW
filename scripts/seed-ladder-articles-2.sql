## JFT-Basic vs JLPT：どちらを選ぶべきか

### JFT-Basicを選ぶケース
- できるだけ早く特定技能1号に移行させたい
- 試験当日に合否がわかるので在留資格申請のスケジュールを立てやすい
- 年6回受験機会があるため、1回落ちてもすぐ次の受験が可能

### JLPTを選ぶケース
- 国際的に認知された資格として残したい
- 時間をかけてしっかり日本語力を身につけさせたい
- すでに受験申込をしている・勉強が進んでいる

