## 企業のサポートが「合否を分ける」3つの理由

### 理由1：日本語学習の環境を作れるか

職場での日本語使用が試験対策の最大の近道です。

**効果的な取り組み：**
- 朝礼・業務指示を日本語で行う（多言語との並行でOK）
- 業務マニュアルを「やさしい日本語」で作成し、日本語を読む習慣をつける
- 週1〜2時間の日本語学習時間を就業時間内に確保する
- N4対策テキスト・JFT-Basic参考書を会社が購入して提供する
- 日本語学習アプリ（無料）の活用を勧める

### 理由2：試験スケジュールを管理できるか

「そろそろ受けさせよう」では遅すぎます。

**企業がやるべきこと：**
- 試験実施スケジュールを先に調べてカレンダーに登録する
- JFT-Basicは**CBT方式で会場予約が必要**。早めに予約しないと希望日に受験できない
- JLPTは**年2回（7月・12月）**。申込期間を逃すと半年待ちになる
- 試験前2週間は残業を減らし、学習時間を確保する

### 理由3：試験費用を会社が負担できるか

受験料の自己負担が障壁になって受験を先延ばしにするケースがあります。

| 試験 | 受験料（国内） |
|------|-------------|
| JFT-Basic | 約7,000円 |
| JLPT N4 | 7,500円（2025年から） |
| 技能測定試験 | 分野・区分により異なる（3,000〜10,000円程度） |

受験料の会社負担は法的義務ではありませんが、合格率向上への投資として検討する価値があります。

