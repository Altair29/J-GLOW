## 介護分野の追加要件

介護分野は、JFT-BasicまたはJLPT N4に加えて、**介護日本語評価試験**への合格が別途必要です。

介護日本語評価試験の概要：
- 年複数回実施（日程は公益社団法人日本介護福祉士養成施設協会が公表）
- 国内・海外の両方で実施
- 介護現場で使われる日本語に特化した内容

技能実習2号（介護）を良好修了した場合は介護日本語評価試験も免除されます。

