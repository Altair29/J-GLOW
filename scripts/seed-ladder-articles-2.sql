## 処遇設計の4つのポイント

### ポイント1：在留資格の変更に連動した昇給・役職設定

特定技能2号取得のタイミングで、処遇の見直しを行うことが重要です。

**参考モデル**

| ステージ | 在留資格 | 役職の目安 | 処遇のポイント |
|---------|---------|----------|-------------|
| 入社〜1年 | 技能実習／育成就労 | スタッフ | 基本給＋生活支援 |
| 1〜3年 | 特定技能1号取得後 | リーダー候補 | 基本給引上げ・資格手当 |
| 3〜5年 | 特定技能1号 | チームリーダー | リーダー手当・賞与対象化 |
| 5年以降 | **特定技能2号取得後** | **班長・副主任** | **日本人スタッフと同等の管理職処遇** |

### ポイント2：スキル・成果ベースの評価制度

外国人スタッフは「年功」より「成果・スキル」で評価されることを望む傾向があります。

**評価制度で整えるべき項目：**
- 業務スキルの習熟度評価（段階別チェックリスト）
- 指導・後輩育成の実績評価（何人を指導したか）
- 日本語能力の向上評価（N3、N2取得などで加点）
- 安全管理・品質管理への貢献度

評価結果は毎年1〜2回、本人にフィードバックする場を設けてください。フィードバックの機会がないと「評価されていない」という感覚になります。

### ポイント3：キャリアパスの「見える化」

入社時・1号取得時・2号取得時の3段階で、「この先どうなれるか」を文書で説明することが重要です。

**キャリアパスシートに盛り込む内容：**
- 各在留資格・役職に求める業務スキル
- 昇給の基準（何ができれば給与が上がるか）
- 会社として応援できるキャリア目標（永住申請、家族帯同等）

### ポイント4：永住・家族帯同のサポート

特定技能2号になると、永住申請や家族の帯同が可能になります。これをサポートすることが、最強の定着施策になります。

**企業が支援できること：**
- 永住申請に必要な書類（在職証明書・収入証明等）の迅速な発行
- 配偶者・子どもの在留資格手続きのサポート（行政書士紹介等）
- 家族向けの生活支援情報の提供（学校・医療機関等）

