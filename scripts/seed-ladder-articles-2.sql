## 試験の難易度と「合格できない」本当の理由

### 日本語試験の合格率

| 試験 | 合格率 | 特徴 |
|------|-------|------|
| JFT-Basic | **約47%** | CBT方式・当日結果判明・年6回受験可 |
| JLPT N4 | **約31.6%** | マークシート・年2回・国際的信頼性高い |

（出典：国際交流基金 2024年7-8月JFT-Basicテスト実施概要報告、2023年第2回JLPTデータ）

JFT-Basicのほうが受験機会が多く、合格率も高い傾向があります。「できるだけ早く特定技能に移行したい」場合はJFT-Basicを推奨します。

### 技能試験が「受からない」本当の理由

試験に落ちる原因として多いのは「技能が不足しているから」ではなく、以下の2点です。

**① 問題文の日本語が読めない**
技能測定試験の問題は日本語で出題されます。N4レベルの基礎的な日本語力はあっても、業務専門用語が入った問題文は読解が難しいケースがあります。

**② 試験対策に時間を割けない**
毎日の業務が忙しく、試験勉強の時間を確保できないまま受験→不合格→再受験のサイクルが続くパターンです。

