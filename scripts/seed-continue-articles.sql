## 「うちは待遇が良いから大丈夫」が通じない時代

外国人材が離職する理由を「給与が安かったのだろう」「合わなかったのだろう」と片付けていませんか。転籍自由化が実現した今、外国人材は待遇の良い職場へ合法的に移れます。

出入国在留管理庁の調査（2023年）によると、特定技能1号外国人の2019〜2022年末までの累計離職率は**16.1%**でした。日本人の新規学卒就職者の3年以内離職率が30%以上であることと比べると低水準ですが、問題は「離職後にどこへ行くか」です。離職者のうち帰国は31.4%、特定技能のまま他社へ転職したのは30.3%——つまり転職先が国内にあります。

さらに深刻なのは、早期退職していない外国人材のうち**53%がモチベーションダウンを経験している**というデータです（ヒューマングローバルホールディングス調査）。辞めていないだけで、心は離れかけている状態です。

## 在留資格別に見る「辞める理由」の違い

### 育成就労・技能実習出身者が辞める理由

育成就労（旧技能実習）で来日した外国人材は、そもそも「もっと良い条件の会社があれば移れる」という選択肢が法的に認められました。定着を阻む主な要因は次の通りです。

**給与が上がらない**：技能が向上しているのに賃金が入社時と変わらない。在留資格変更のたびに給与交渉の機会がありますが、企業側が見直しをしない場合、転籍を選ぶ動機になります。

**キャリアが見えない**：特定技能1号への移行後、2号を目指すためには班長・リーダー経験や試験合格が必要です。その道筋を会社が示さないと、「この会社にいても先がない」と感じます。

**生活上の孤立**：日本語でのコミュニケーション、医療、役所手続き——これらのサポートがない職場は、孤独感から離職を選ぶ人が多いです。

### 技術・人文知識・国際業務（技人国）が辞める理由

大卒・専門卒の高度外国人材が辞める理由は、育成就労とは異なります。

**ポジションと評価が見合わない**：日本語能力や専門スキルが高いのに、「外国人だから」という理由で昇進が滞るケースがあります。

**キャリアパスの不透明さ**：永住権を視野に入れると10年の在留が必要です。その間にどのようなポジションに就けるか、給与がどう上がるかが見えないと、外資系や他の日本企業へ転職します。

**日本の職場文化との摩擦**：残業文化、曖昧な指示、合意形成プロセスの長さ——これらを「非効率」と感じる外国人材は多く、より合理的な職場を求めて転職します。

### 特定技能1号が辞める理由

特定技能は転職が自由なため、離職リスクが最も顕在化しています。

**スキルの移転制約**：内閣府2024年経済財政白書によると、他社から転籍した特定技能労働者は、スキルが評価されない「スキルの移転制約」により賃金が低下する傾向があります。逆に言えば、自社で育てた人材を手放すと、その人は一時的に評価が下がります。この事実を本人に伝え、長期雇用のインセンティブとして活用することが重要です。

**2号移行支援がない**：特定技能1号の在留上限は通算5年（2025年10月改正で一定条件下では6年まで延長可）。その後も働き続けるには2号移行が必要ですが、企業がサポートをしないと帰国を選びます。

### 留学生（資格外活動）が辞める理由

留学生は卒業後に在留資格を変更する必要があります。「良い会社に就職できれば技人国に変えられる」という事情から、条件の良い会社を探し続けています。内定を出したとしても、卒業前後の手続きをサポートしないと、より手続きサポートが充実した会社に流れます。

