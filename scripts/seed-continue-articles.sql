## 違反の多くは「知らなかった」から起きる

外国人を雇用する企業への行政指導・処分は年々増加しています。2024年改正入管法では**不法就労助長罪が懲役5年以下・罰金500万円以下に厳罰化**されました。「知らなかった」は免責になりません。

問題は、多くの違反が悪意ではなく「制度を正確に知らなかった」「管理が追いついていなかった」ことで起きていることです。本記事では、よくある違反パターンを整理し、具体的な対策を示します。

## よくある違反パターン7選

### 1. 在留資格の確認不足・期限切れの放置

**何が問題か**：在留カードの有効期限を確認せず、オーバーステイの外国人を雇用し続けるケース。

**結果**：不法就労助長罪（企業）。外国人本人は退去強制の対象になります。

**対策**：採用時・在留更新時・年1回の定期確認。在留カードのコピーを台帳に保管し、有効期限をカレンダーで管理。

### 2. 在留資格の範囲外の業務をさせる

**何が問題か**：技人国で採用した外国人に、在留資格の活動範囲外の業務（製造ラインの現場作業のみ等）をさせるケース。育成就労・特定技能で認められた以外の業務への従事も違反です。

**結果**：不法就労助長罪の対象。外国人本人の在留資格取り消しの事由にもなります。

**対策**：採用時に業務内容と在留資格の整合性を確認。異動・業務変更の際も在留資格との整合を事前確認。

### 3. 賃金不払い・最低賃金違反

**何が問題か**：残業代の不払い、最低賃金以下での雇用。技能実習生の失踪原因の約44%が「最低賃金違反・契約賃金違反・賃金からの過大控除・割増賃金不払い」です（出入国在留管理庁調査）。

**結果**：労働基準法違反・監理支援機関からの指導・受入れ停止処分の可能性。

**対策**：毎年10月の最低賃金改定時に給与を見直す。残業代の計算方法を正確に把握。住居費等の控除は適正範囲（本人同意・合理的金額）で。

### 4. 雇用状況届出の未提出

**何が問題か**：外国人を採用・退職させた際にハローワークへの「外国人雇用状況届出」を提出しないケース。

**結果**：30万円以下の罰金。

**対策**：外国人の入退職時に必ずハローワークへ届出（雇用保険の資格取得・喪失届と同時に手続き可能）。

### 5. 支援計画の不実施（特定技能・育成就労）

**何が問題か**：特定技能1号の受入れ機関または育成就労の受入れ機関は、定められた支援（生活オリエンテーション・日本語学習支援・相談窓口設置等）の実施が義務です。これを実施しないケース。

**結果**：特定技能・育成就労の受入れ停止処分。

**対策**：支援計画書を作成し、実施記録を保管。登録支援機関・監理支援機関との連携で実施漏れを防ぐ。

### 6. 定期報告・届出の漏れ

**何が問題か**：特定技能・育成就労では、3〜6ヶ月ごとに入管への定期報告が義務です。これを失念するケース。

**結果**：改善指導・悪質な場合は受入れ停止。

**対策**：報告期限をカレンダーで管理。監理支援機関・登録支援機関からのリマインドを活用。

### 7. 強制的な貯金・保険商品への加入強制

**何が問題か**：「帰国のための貯金」などの名目で強制的に給与から控除したり、特定の保険商品への加入を強制するケース。

**結果**：育成就労・技能実習の計画認定取消し。

**対策**：控除は本人の書面同意のもとで合理的な範囲のみ。保険加入強制は禁止。

## コンプライアンス体制の作り方

### 最低限整備すべき書類・記録

- 外国人社員台帳（在留資格・在留期限・国籍・パスポート番号等）
- 在留カードのコピー（採用時・更新時）
- 雇用契約書（日本語＋母国語の二言語）
- 労働条件通知書
- 支援計画書と実施記録（特定技能・育成就労）
- 定期報告書の控え

### 年に1回の内部チェックリスト

- [ ] 全外国人社員の在留期限を確認し、3ヶ月以内に期限が来る者のリストアップ
- [ ] 業務内容と在留資格の整合性確認（特に業務変更・異動後）
- [ ] 最低賃金の改定に伴う給与水準の確認
- [ ] 支援計画の実施状況の点検
- [ ] 定期報告の提出済みを確認

### 監理支援機関・登録支援機関との連携

育成就労・特定技能の場合、監理支援機関または登録支援機関との連携が義務です。これらの機関は法令違反のリスクを下げる「共同管理者」でもあります。不明点は積極的に相談することで、企業単独では気づかない問題を未然に防げます。

