## 「最低賃金以上払っている」では不十分な時代

外国人材の給与について、「最低賃金を守っているから問題ない」と考えている経営者はまだ多いです。しかし、法的には最低賃金の遵守だけでは不十分なケースがあります。

厚生労働省の統計（2023年）によると、外国人労働者の平均月額賃金は**232,600円**で、日本人平均の318,300円と比べて約85,700円の差があります。一方で、**特定技能の省令では「日本人が従事する場合の報酬と同等以上」が義務付けられています**（特定技能雇用契約及び一号特定技能外国人支援計画の基準等を定める省令 第1条第2項第3号）。

