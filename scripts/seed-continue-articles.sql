## 更新申請の実務フロー

### 申請可能時期
在留期間の満了日の**3ヶ月前から申請可能**です。2〜4月は入管の繁忙期のため、対象者がいる場合は早めに着手します。

### 企業が準備する書類（技人国・特定技能共通）
- 在留資格変更（更新）許可申請書
- 雇用契約書（または労働条件通知書）のコピー
- 直近の源泉徴収票
- 会社の登記簿謄本（カテゴリー3・4の場合）
- 直近2期分の決算書（カテゴリー3・4の場合）

### 申請できる人
本人または申請取次の資格を持つ行政書士・社会保険労務士、または「申請等取次者」として認められた企業担当者（事前届出が必要）。

### オンライン申請の活用
2022年より、一部の在留資格申請がオンラインで可能になっています。カテゴリー1・2の企業は特に積極的に活用してください。

