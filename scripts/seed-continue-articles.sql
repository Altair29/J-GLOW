## 育成就労制度は「育てて長く雇う」ための制度

育成就労制度（2027年4月施行）は、技能実習制度と異なり「特定技能1号への移行を前提とした人材育成」を目的としています。つまり企業は最初から「3〜5年後に特定技能1号として自社で活躍してもらう」というゴールを持って採用・育成する制度設計になっています。

このルートを整理すると以下の通りです。

**育成就労（最長3年）→ 特定技能1号（最長5年、条件により6年）→ 特定技能2号（上限なし）→ 永住申請可能**

育成就労から特定技能1号への移行には、育成就労計画の良好な修了と、分野によっては技能評価試験・日本語試験の合格が必要です（同分野での移行の場合、試験が免除されるケースあり）。

## 特定技能1号→2号移行の現実

2025年1月時点で特定技能2号の取得者は全分野合計で**1,047人**です（外食業134人を含む）。制度が2019年にスタートして7年が経つにもかかわらず、まだ非常に少ない数字です。

なぜこれほど少ないのか。2号移行には技能試験合格に加え、**班長・リーダーとして複数の作業員を指導・監督する実務経験**が必要です。この経験を積める環境を企業が用意していないケースが多いことが主因です。

企業がやるべきことは明確です。「2号を目指す人材を見極め、リーダー職を与え、試験勉強をサポートする」——これだけで他社との差別化になります。

## 分野別・特定技能2号の主な要件

### 製造業（素形材・産業機械・電気電子情報関連）
- 国内製造業現場での**3年以上の実務経験**
- 製造分野特定技能2号評価試験（3区分：機械金属加工・電気電子機器組立て・金属表面処理）合格
- 試験は日本語のみで実施（ルビなし）

### 建設
- 複数の作業員を指導しながら**工程管理する者**（班長・職長等）としての実務経験
- 建設分野特定技能2号評価試験または技能検定1級に合格

### 外食業
- **副店長・サブマネージャー等として2年以上の実務経験**
- 外食業特定技能2号技能測定試験合格（外食業は合格率約60%と比較的高い、他分野は概ね30%程度）
- JLPT N3以上

### 介護
- ※介護は特定技能2号の対象外（介護福祉士等の別途ルートあり）

## 企業が今すぐできる2号移行サポート

### ①逆算スケジュールの作成

育成就労で入社した人材が2号を取得するまでのタイムラインを入社時から描きます。

例：製造業の場合
- 入社〜3年：育成就労（技能実習相当）
- 3年目：特定技能1号に移行
- 1〜2年目：班長補佐として現場経験を積む
- 2〜3年目：班長就任＋2号試験の勉強開始
- 5年目（特定技能1号通算）：2号試験受験・申請

このスケジュールを入社時に本人に渡し、「5年後にはここまで行ける」と見せることが重要です。

### ②日本語学習の支援

2号試験はすべて日本語で出題され、漢字にルビがありません。N3〜N2相当の日本語力が実質的に必要です。企業として以下のサポートが有効です。

- 日本語学習アプリ（Duolingo等）の費用補助
- 週1回の社内日本語勉強会
- JLPT受験料の会社負担
- 合格時の一時金（例：N4合格で5,000円、N3合格で10,000円）

### ③試験対策の機会提供

試験合格には業務だけでなく学習時間が必要です。

- 試験前の特別休暇（1〜2日）の付与
- 過去問・テキスト費用の会社負担
- 同分野の先輩合格者による勉強会

## 永住申請に向けた長期計画

特定技能2号で在留すると、就労期間が永住申請の10年要件に算入されます。育成就労から数えると、順調に進んだ場合8〜10年程度で永住申請の要件に近づきます。

永住者になると、就労制限がなくなり会社都合での異動・昇格が自由になります。「永住まで応援する」という姿勢を持つ会社は、外国人材から高く評価されます。

## まとめ

育成就労→特定技能1号→2号→永住というキャリアラインを企業が描き、そのための環境（リーダー職・日本語支援・試験サポート）を整えることが、最も効果的な定着戦略です。2号取得者がまだ1,047人という現状は、逆に「今から整備すれば差別化できる」チャンスでもあります。

