## 在留資格別の給与設定ルール

### 育成就労・技能実習の場合

育成就労期間中は、技能習得中という位置づけから最低賃金以上の支払いが基本となります。ただし以下の点に注意が必要です。

- 同一業務に従事する日本人労働者と著しく異なる賃金設定は問題になる可能性があります
- 技能実習から特定技能1号に移行した時点で、日本人と同等以上の賃金が法的義務になります
- 住居費・食費の控除は、適正な範囲内で本人の同意を得た上で行う必要があります

### 特定技能1号・2号の場合

**法的に明確な義務**があります。「日本人が従事する場合の報酬の額と同等以上」——これは実質的に、同じ業務・同程度の経験年数の日本人社員の給与と比較して設定する必要があることを意味します。

具体的な目安は「日本人高卒者の給与水準と同等程度以上」とされていますが、経験や技能に応じた設定が求められます。同一労働同一賃金（2020年4月施行）も外国人に適用されます。

### 技術・人文知識・国際業務（技人国）の場合

在留資格申請の審査において、**「日本人と同等額以上の報酬」が許可基準**の一つです。日本人が同職種に就く場合の給与水準を下回ると、在留資格の申請が不許可になる可能性があります。

## 昇給設計：定着につながる給与の見直し方

定着率を上げるためには、在留資格の更新・変更タイミングに合わせた昇給レビューが効果的です。

**入社1年目**：スキル習得確認、基本給の設定水準を確認

**在留資格変更時（技能実習→特定技能）**：
- 特定技能移行で法的に同等待遇が求められるため、この時点での昇給は必須
- 目安として月1〜3万円程度の見直しをしている企業が多い

**技能向上・試験合格時**：
- 特定技能2号の試験合格を目指す段階では、班長・リーダー職への昇格と合わせた昇給が定着に直結
- 「合格したら昇給する」という約束を書面で明示することが重要

**在留更新ごと**：
- 継続的な勤務実績に対する定期昇給のルールを就業規則に明記
- 「外国人だから」という理由での据え置きは同一労働同一賃金の観点から問題になる可能性あり

## 給与以外の「待遇」も差別的取扱いは禁止

特定技能の規定では、「外国人であることを理由として、報酬の決定、教育訓練の実施、福利厚生施設の利用その他の待遇について差別的な取扱いをしていないこと」が義務です。

具体的には以下が対象です。
- 研修・教育訓練の受講機会
- 社員旅行・懇親会等の福利厚生
- 健康診断（特定技能・技人国ともに社会保険加入が義務）
- 育児休業・介護休業制度の適用

## 「給与を上げると転籍リスクが下がる」というデータ

