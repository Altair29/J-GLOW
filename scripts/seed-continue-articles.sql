## 「頑張っているのに評価されない」が最大の離職動機

外国人材の離職理由調査（ヒューマングローバルホールディングス・ウィルテック等共同調査、2021年）で、「上司のマネジメント・指導に対する不満」と「業務内容のミスマッチ」が上位に挙がっています。これらは給与以前の問題——「何をすれば評価されるかわからない」「頑張っても変わらない」という評価制度の不透明さが根本にあります。

特定技能2号への移行には、「複数の作業員を指導・監督しながら作業工程を管理する者」としての実務経験が必要です。つまり**班長・リーダー・副店長などの役職を与えなければ、そもそも2号への道が開けない**のです。ポジションの引き上げは人情の問題でなく、制度上の必要条件です。

## 外国人材に合わせた等級制度の設計

### ステップ1：業務レベルを4段階で定義する

| レベル | 業務内容 | 対応する在留資格 |
|--------|----------|----------------|
| L1 | 指示のもとで決まった業務を実施 | 育成就労1〜2年目 |
| L2 | 自律的に業務遂行・後輩指導補佐 | 特定技能1号前期 |
| L3 | 班・チームのリーダー、工程管理補佐 | 特定技能1号後期・2号準備 |
| L4 | 管理業務・班長・副店長等の役職 | 特定技能2号要件の核心 |

このレベル表を多言語（最低でも英語・母国語）で本人に渡すことが重要です。

### ステップ2：レベルアップの基準を明文化する

「頑張れば上がる」ではなく、具体的な基準を設けます。

**L1→L2 の基準例**
- 在籍1年以上
- 技能実習2号良好修了 または 特定技能技能試験合格
- 欠勤率3%以下
- 直属上司の評価A以上

**L2→L3 の基準例**
- 在籍2年以上
- N4以上の日本語能力（証明書提出）
- 後輩スタッフ2名以上への業務指導実績
- 品質・安全の基準を満たした稼働実績

**L3→L4 の基準例**
- 在籍3年以上
- 特定技能2号試験の受験意向確認
- 班長・リーダーとして3ヶ月以上の実績
- 品質トラブルゼロ期間の確認

### ステップ3：キャリアアッププランを書面で交付する

特定技能の支援計画では「キャリアアッププラン」の作成・交付が推奨されています。内容の例として出入国在留管理庁の運営要領では以下が示されています。

- 想定されるキャリアルート
- 各レベルの業務内容と習熟の目安となる年数
- レベルアップに必要な経験・実績・資格・検定等

このプランは採用時・在留資格変更時の両方で交付し、本人の署名を得て保管します。

## ポジション引き上げの実務ポイント

### 班長・リーダー職の与え方

特定技能2号の要件である「複数の作業員を指導・監督する者」は、同一企業内に限らず、現場での役割で判断されます（JACのQ&A参照）。小規模企業でも「この現場でのリーダー」として実績を積ませることが可能です。

### 評価面談を年2回実施する

外国人材は「自分がどう評価されているか」を特に不安に感じます。年2回（6ヶ月ごと）の評価面談を設け、レベルアップまでの具体的なギャップと次のアクションを伝えます。面談には通訳を入れるか、多言語の評価シートを使うことで認識齟齬を防げます。

### 役職手当を設ける

班長・リーダー職に就任した際に「役職手当（例：月5,000〜15,000円）」を設けることで、ポジションアップへの動機付けになります。この手当は特定技能2号試験の実務経験証明においても有利に働きます。

## 日本人スタッフへの配慮

外国人材をリーダーに昇格させる際、日本人スタッフとの関係に配慮が必要なケースがあります。

- 昇格理由を明確に全員に伝える（「試験に合格したから」「実績があるから」という客観的根拠）
- 日本人スタッフへのキャリアパスも同時に見直す
- 多文化チームのマネジメント研修を管理職向けに実施する

## まとめ

外国人材のポジション引き上げは「制度的に必要な要件」であり、同時に「最も効果的な定着施策」でもあります。レベル定義→基準の明文化→キャリアアップランの交付という3ステップを整備し、本人が「この会社でキャリアを築ける」と実感できる環境を作ることが、長期定着の核心です。

