## 技人国の外国人材が転職する理由は「成長できない」から

技術・人文知識・国際業務（技人国）の在留資格を持つ外国人材は、大学・専門学校を卒業した高度人材です。育成就労・特定技能と異なり、転職の自由度が高く、スキルと意欲があれば外資系・大手企業へも移れます。

技人国で入社した外国人材が離職する主な理由は「日本人と比べてポジションが上がらない」「評価基準が不透明」「日本の職場文化との摩擦」の3点です。

また内閣府の分析では、高技能外国人のうち日本在留歴が長い人は永住資格に移行している可能性が高く、就労資格で残っている高技能外国人は「まだキャリアを構築中の段階」であるとされています。この層が「この会社で永住まで行ける」と感じるかどうかが定着の鍵です。

## 在留資格の更新管理：まず「期限切れ」を防ぐ

技人国の在留期間は「5年・3年・1年・3ヶ月」のいずれかです。初回は1年が付与されるケースも多く、更新を繰り返すことで長期に移行していきます。

更新を忘れると不法滞在になり、企業も不法就労助長罪の対象になります。管理の基本は以下の通りです。

- **在留カードの有効期限を人事台帳に記録**
- **更新申請は満了日の3ヶ月前から可能**（繁忙期の2〜4月は審査が混雑するため早めに）
- **転職・異動・業務変更時は「契約機関に関する届出」が必要**（14日以内）
- **更新が通らないケース**：業務内容が在留資格と一致しない、会社の経営状態が悪化している、本人に法令違反がある

更新申請の必要書類は企業規模（カテゴリー1〜4）によって異なります。上場企業・大企業（カテゴリー1・2）は書類が少なく、中小企業（カテゴリー3・4）は決算書等の提出が必要です。

## 昇格・ポジションアップの設計

技人国の外国人材は「専門知識を活かした業務」に従事することが在留資格の条件です。現場作業のみを担当させると在留資格の取り消し事由になりかねません。業務内容と在留資格の整合性を保ちながら昇格させる必要があります。

**昇格のステップ例（IT系技術職の場合）**：

- 1〜2年目：担当業務の習熟、日本語N3取得
- 3〜4年目：プロジェクトリーダー補佐、N2取得
- 5〜6年目：プロジェクトリーダー、部門内の外国人後輩の指導
- 7年目〜：シニアエンジニア・管理職候補

このようなキャリアパスを入社時に提示し、毎年の面談で進捗を確認します。

## 永住申請をサポートする

技人国で働く外国人が永住申請を検討するのは、在留歴が5〜10年を超えたあたりです。永住の要件は複数ありますが、主なものは以下です。

- 原則として**10年以上継続して日本に在留**（就労資格での在留は最低5年）
- 素行が善良
- 独立の生計を営む資産または技能

企業として永住申請をサポートすることで、外国人材に「この会社に長くいる価値がある」と伝えられます。具体的には：

- 在職証明書・所得証明書の発行サポート
- 行政書士への相談窓口の紹介
- 申請手続きのための有給休暇（1〜2日）の付与

## 家族帯同への配慮

技人国の外国人材は家族帯同が可能です（配偶者・子に「家族滞在」の在留資格が付与）。しかし住居・子どもの教育・配偶者の就労（週28時間まで資格外活動許可）など、生活面の課題も多くあります。

企業として支援できることは限られますが、「社内に相談できる人がいる」と感じてもらうことが重要です。地域の国際交流協会や行政の外国人相談窓口の情報を入社時に渡すだけでも違います。

