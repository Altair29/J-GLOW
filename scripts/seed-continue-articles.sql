## 留学生採用は「卒業後の手続き」から始まる

留学生を採用する企業にとって、内定を出すことがゴールではありません。卒業後に「留学」から「技術・人文知識・国際業務（技人国）」や「特定技能」に在留資格を変更する手続きを支援しなければ、そもそも働き始めることができません。

2019年に日本の「技術・人文知識・国際業務」の在留資格を取得した外国人の**32%が国内留学からの在留資格変更**であるというデータがあります（佐藤2022、内閣府2024年経済財政白書引用）。留学生は高技能外国人の重要な供給源です。

一方で、ビザの切替え手続きに慣れていない企業では「何をいつまでにやればよいかわからない」という混乱が生じ、入社前に他社に流れるケースがあります。

## 留学生採用の在留資格変更スケジュール

**4月入社の場合（目安）**：

| 時期 | やること |
|------|----------|
| 10〜11月 | 内定出し・雇用契約書（労働条件通知書）の交付 |
| 12〜1月 | 「卒業見込み証明書」＋雇用契約書で変更申請 |
| 1〜3月 | 審査期間（1〜2ヶ月が目安、繁忙期はさらに延びる） |
| 3月 | 卒業証明書の提出・在留資格変更許可 |
| 4月 | 就労開始 |

**重要な注意点**：
- 内定を出してから申請、という順番。ビザが取れてから内定ではありません
- 在留期限が3月末の場合、12月〜1月には申請準備を完了させる必要あり
- 2〜4月は入管が混雑するため、早期申請が安全

## 技人国への変更で審査される3つのポイント

### ①業務内容と専攻の関連性

大学・専門学校で学んだ内容と、入社後の業務に一定の関連性が必要です。

- IT系学部卒→エンジニア職：関連性が高く通りやすい
- 経営学部卒→営業職：関連性ありと判断される
- 文学部卒→工場の製造ラインのみ：不許可になる可能性あり

**専門学校卒の注意**：2024年3月から「外国人留学生キャリア形成促進プログラム」に認定された専門学校の卒業生は、大学卒業者と同等に扱われる制度が整備されました。ただし、専攻と業務の関連性は引き続き審査されます。

### ②日本人と同等以上の報酬

技人国の許可基準の一つです。同職種の日本人新卒と同等以上の給与を設定してください。「外国人だから安くて良い」は審査で問題になります。

### ③会社の実態と安定性

設立間もない企業や経営が不安定な企業は、審査が厳しくなります。カテゴリー3・4（中小企業等）は決算書・事業計画書の提出が求められます。

## 留学生特有の「特定活動46号」という選択肢

日本語能力が高い留学生（大学卒業・日本語能力N1相当）で、製造業や飲食業の現場業務（接客等）に就きたい場合、技人国では認められない業務でも「特定活動46号」で対応できるケースがあります。ただし要件が厳しく（日本の4年制大学卒・N1相当等）、対象は限られます。

## 卒業後の「定着」に向けたサポート

在留資格の変更が完了したあと、留学生出身の外国人材が辞める理由は技人国全般と共通ですが、特有の課題もあります。

**「学生気分が抜けない」問題**：日本の企業文化（ほうれんそう、会議の作法、暗黙のルール）は、留学生には特に難解です。入社後3〜6ヶ月は専任のメンター（先輩社員）をつけることが有効です。

**アルバイト時代との給与差への誤解**：留学中は週28時間以内のアルバイトだったため、社会保険料の天引きで手取りが思ったより少ないと感じるケースがあります。入社前に手取り計算を一緒に確認するひと手間が大切です。

